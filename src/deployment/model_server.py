class PredictionRequest(BaseModel):
    """Single prediction request with input features."""
    features: dict[str, float | int | str] = Field(
        ...,
        description="Feature key-value pairs. All required features must be present.",
        example={"age": 55, "feature_1": 0.82, "feature_2": -0.14},
    )
    patient_id: str | None = Field(
        None,
        description="Optional de-identified patient identifier for audit logging.",
    )
    request_id: str | None = Field(
        None,
        description="Client-provided request ID for tracing.",
    )
    confidence_threshold_override: float | None = Field(
        None,
        ge=0.0,
        le=1.0,
        description="Override default confidence threshold for this request.",
    )
    model_version: str | None = Field(
        None,
        description="Request a specific model version. Defaults to current.",
    )

    @field_validator("features")
    @classmethod
    def features_not_empty(cls, v: dict) -> dict:
        if not v:
            raise ValueError("features dict cannot be empty")
        return v


class BatchPredictionRequest(BaseModel):
    """Batch prediction request."""
    requests: list[PredictionRequest] = Field(
        ...,
        description="List of prediction requests.",
        min_length=1,
    )

    @field_validator("requests")
    @classmethod
    def batch_size_check(cls, v: list) -> list:
        if len(v) > MAX_BATCH_SIZE:
            raise ValueError(f"Batch size {len(v)} exceeds maximum {MAX_BATCH_SIZE}")
        return v


class PredictionResponse(BaseModel):
    """Prediction response with probability, binary decision, and metadata."""
    request_id: str
    prediction_id: str
    model_name: str
    model_version: str
    probability: float = Field(..., ge=0.0, le=1.0,
                               description="Predicted probability for positive class.")
    predicted_class: int = Field(..., description="Binary prediction (0 or 1).")
    confidence_threshold: float
    high_confidence: bool = Field(
        ...,
        description="True if probability is above (1 - threshold) or below threshold "
                    "i.e., unambiguous prediction.",
    )
    processing_time_ms: float
    timestamp: str
    compliance_note: str = (
        "This prediction is generated by an investigational AI system. "
        "Clinical decisions must be reviewed by qualified healthcare professionals."
    )


class HealthResponse(BaseModel):
    """Health check response."""
    status: str          # "healthy" | "degraded" | "unhealthy"
    model_loaded: bool
    model_name: str
    model_version: str
    uptime_seconds: float
    total_predictions_served: int
    last_prediction_at: str | None
    warnings: list[str]


# ---------------------------------------------------------------------------
# Model Registry
# ---------------------------------------------------------------------------

class ModelRegistry:
    """In-process model version registry."""

    def __init__(self):
        self._models: dict[str, Any] = {}
        self._current_version: str = MODEL_VERSION
        self._load_count: dict[str, int] = {}

    def load_model(self, version: str = MODEL_VERSION, path: str | None = None) -> None:
        """Load a model version into registry."""
        model_path = path or MODEL_PATH

        try:
            if MLFLOW_AVAILABLE and not Path(model_path).exists():
                # Try MLflow model URI — retry on transient connection errors
                _mlflow_retries = int(os.environ.get("MLFLOW_CONNECT_RETRIES", "3"))
                for _attempt in range(1, _mlflow_retries + 1):
                    try:
                        model = mlflow.pyfunc.load_model(model_path)
                        logger.info("Loaded MLflow model: %s", model_path)
                        break
                    except Exception as _mlflow_err:
                        if _attempt < _mlflow_retries:
                            logger.warning(
                                "MLflow connection attempt %d/%d failed: %s — retrying",
                                _attempt, _mlflow_retries, _mlflow_err,
                            )
                            import time as _time
                            _time.sleep(2 ** _attempt)
                        else:
                            raise
            else:
                # Fallback: load sklearn/pickle model
                import joblib
                model = joblib.load(model_path)
                logger.info("Loaded joblib model from: %s", model_path)

            self._models[version] = model
            self._load_count[version] = 0
            logger.info("Model version '%s' ready", version)

        except FileNotFoundError:
            logger.warning(
                "Model not found at '%s'. Server will start but predictions unavailable. "
                "Set MODEL_PATH environment variable to model location.",
                model_path,
            )
            self._models[version] = None

    def get_model(self, version: str | None = None) -> Any:
        v = version or self._current_version
        model = self._models.get(v)
        if model is None:
            raise HTTPException(
                status_code=status.HTTP_503_SERVICE_UNAVAILABLE,
                detail=f"Model version '{v}' not loaded or unavailable.",
            )
        self._load_count[v] = self._load_count.get(v, 0) + 1
        return model

    def set_current_version(self, version: str) -> None:
        if version not in self._models:
            raise ValueError(f"Version '{version}' not loaded")
        self._current_version = version
        logger.info("Current model version set to: %s", version)

    @property
    def current_version(self) -> str:
        return self._current_version

    @property
    def is_loaded(self) -> bool:
        return bool(self._models.get(self._current_version))

    def available_versions(self) -> list[str]:
        return list(self._models.keys())


