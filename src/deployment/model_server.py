class PredictionAuditLogger:
    """
    JSONL prediction audit logger for post-market surveillance.
    Every prediction is logged with timestamp, model version, input hash,
    and result — without storing raw PHI.
    """

    def __init__(self, log_path: str = PREDICTION_LOG_PATH):
        self.log_path = Path(log_path)
        self.log_path.parent.mkdir(parents=True, exist_ok=True)
        self._buffer: list[dict] = []

    def log(
        self,
        prediction_id: str,
        model_version: str,
        input_hash: str,
        probability: float,
        predicted_class: int,
        processing_time_ms: float,
        patient_id_hash: str | None = None,
        request_id: str | None = None,
    ) -> None:
        entry = {
            "prediction_id": prediction_id,
            "timestamp": datetime.utcnow().isoformat(),
            "model_name": MODEL_NAME,
            "model_version": model_version,
            "input_feature_hash": input_hash,    # hash only — no raw features stored
            "patient_id_hash": patient_id_hash,  # hash only — no raw ID stored
            "request_id": request_id,
            "probability": round(probability, 6),
            "predicted_class": predicted_class,
            "processing_time_ms": round(processing_time_ms, 2),
        }

        if LOG_PREDICTIONS:
            with open(self.log_path, "a") as f:
                f.write(json.dumps(entry) + "\n")

        self._buffer.append(entry)

        # Keep buffer bounded
        if len(self._buffer) > 10000:
            self._buffer = self._buffer[-5000:]


