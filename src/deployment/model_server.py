@app.get(
    "/health",
    response_model=HealthResponse,
    tags=["health"],
    summary="Server health check",
)
async def health_check() -> HealthResponse:
    warnings = []
    if not registry.is_loaded:
        warnings.append("Model not loaded — inference unavailable")
    if not MLFLOW_AVAILABLE:
        warnings.append("MLflow unavailable — running without experiment tracking")

    return HealthResponse(
        status="healthy" if registry.is_loaded else "degraded",
        model_loaded=registry.is_loaded,
        model_name=MODEL_NAME,
        model_version=registry.current_version,
        uptime_seconds=round(time.time() - _start_time, 1),
        total_predictions_served=_prediction_count,
        last_prediction_at=_last_prediction_at,
        warnings=warnings,
    )


@app.get(
    "/health/ready",
    tags=["health"],
    summary="Readiness probe (Kubernetes)",
    status_code=200,
)
async def readiness_probe():
    if not registry.is_loaded:
        raise HTTPException(
            status_code=status.HTTP_503_SERVICE_UNAVAILABLE,
            detail="Model not loaded",
        )
    return {"ready": True}


@app.get(
    "/health/live",
    tags=["health"],
    summary="Liveness probe (Kubernetes)",
    status_code=200,
)
async def liveness_probe():
    return {"alive": True, "timestamp": datetime.utcnow().isoformat()}


