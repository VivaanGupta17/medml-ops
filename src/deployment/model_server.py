@app.post(
    "/predict",
    response_model=PredictionResponse,
    tags=["inference"],
    summary="Single prediction",
    description=(
        "Run inference on a single input. Input features are validated, "
        "prediction is logged for audit, and result includes probability and binary decision. "
        "**All predictions are logged for post-market surveillance.**"
    ),
)
async def predict(request: PredictionRequest) -> PredictionResponse:
    global _prediction_count, _last_prediction_at
    start = time.time()
    prediction_id = str(uuid.uuid4())
    request_id = request.request_id or str(uuid.uuid4())

    model = registry.get_model(request.model_version)
    threshold = request.confidence_threshold_override or CONFIDENCE_THRESHOLD

    try:
        feature_array = _features_to_array(request.features)
        y_prob = float(model.predict_proba(feature_array)[0, 1])
        y_pred = int(y_prob >= threshold)
    except AttributeError:
        # MLflow pyfunc predict
        import pandas as pd
        df = pd.DataFrame([request.features])
        result = model.predict(df)
        y_prob = float(result[0]) if hasattr(result, "__iter__") else float(result)
        y_pred = int(y_prob >= threshold)
    except Exception as e:
        logger.error("Inference error: %s", e, exc_info=True)
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Inference failed: {str(e)}",
        )

    processing_time = (time.time() - start) * 1000
    _prediction_count += 1
    _last_prediction_at = datetime.utcnow().isoformat()

    # Audit log
    audit_logger.log(
        prediction_id=prediction_id,
        model_version=registry.current_version,
        input_hash=_hash_features(request.features),
        probability=y_prob,
        predicted_class=y_pred,
        processing_time_ms=processing_time,
        patient_id_hash=_hash_patient_id(request.patient_id),
        request_id=request_id,
    )

    return PredictionResponse(
        request_id=request_id,
        prediction_id=prediction_id,
        model_name=MODEL_NAME,
        model_version=registry.current_version,
        probability=round(y_prob, 6),
        predicted_class=y_pred,
        confidence_threshold=threshold,
        high_confidence=y_prob > (1 - threshold * 0.5) or y_prob < (threshold * 0.5),
        processing_time_ms=round(processing_time, 2),
        timestamp=datetime.utcnow().isoformat(),
    )


@app.post(
    "/predict/batch",
    tags=["inference"],
    summary="Batch prediction",
    description=f"Run inference on multiple inputs in one request. Max batch size: {MAX_BATCH_SIZE}.",
)
async def predict_batch(batch: BatchPredictionRequest) -> list[PredictionResponse]:
    return [await predict(req) for req in batch.requests]


