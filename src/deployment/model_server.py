def _hash_features(features: dict) -> str:
    """SHA-256 hash of input features for audit trail (no raw PHI)."""
    content = json.dumps(features, sort_keys=True).encode()
    return hashlib.sha256(content).hexdigest()[:16]


def _hash_patient_id(patient_id: str | None) -> str | None:
    if not patient_id:
        return None
    return hashlib.sha256(patient_id.encode()).hexdigest()[:16]


def _features_to_array(features: dict[str, float | int | str]) -> np.ndarray:
    """Convert features dict to numpy array, skipping non-numeric."""
    numeric = {k: float(v) for k, v in features.items()
               if isinstance(v, (int, float))}
    return np.array(list(numeric.values()), dtype=np.float32).reshape(1, -1)


